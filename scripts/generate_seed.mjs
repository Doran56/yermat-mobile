// Tables to skip
const SKIP = new Set(['push_subscriptions', 'bar_rewards']);

const parts = [];

parts.push(`-- Seed migration: import from old fizzy-walrus database (exported 2026-05-18)
-- Generated by scripts/generate_seed.mjs
-- DO NOT EDIT MANUALLY

SET session_replication_role = replica; -- disable FK triggers during bulk import
`);

// challenge_types: delete existing seeded rows (different UUIDs) then re-insert
{
  const { headers, rows } = readCSV('challenge_types');
  parts.push(`-- challenge_types: replace seeded rows (UUID mismatch with old data)
DELETE FROM public.challenge_types;
${insertBlock('challenge_types', headers, rows)}
`);
}

// All other tables in order (challenge_types already done)
for (const table of TABLES.slice(1)) {
  const { headers, rows } = readCSV(table);
  parts.push(`-- ${table}: ${rows.length} rows
${insertBlock(table, headers, rows)}
`);
}

parts.push(`SET session_replication_role = DEFAULT;
`);

const sql = parts.join('\n');
writeFileSync(OUT, sql, 'utf8');
console.log(`✓ Written to ${OUT}`);
console.log(`  Total size: ${(sql.length / 1024).toFixed(1)} KB`);
